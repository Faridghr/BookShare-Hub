    public int isValid_input_register(){ //login if username exist '1' else if empty '2' ... else '0'
        String username = usernameTextField1.getText();
        List<User> userList = DatabaseConnector.getAllUsers();
        boolean userExist = false;
        for (User user : userList) {
            if (user.getUsername().equals(username)) {
                userExist = true;
            }
        }
        if(userExist){
           return 1; 
        }
        else if(usernameTextField1.getText().equals("") || emailTextField.getText().equals("") || passwordTextField1.getText().equals("")){
           return 2;
        }
        else
           return 0;
    }
    
