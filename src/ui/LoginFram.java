    private void searchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchButtonActionPerformed
        // TODO add your handling code here:
        
        // SEARCH
        String searchBy = buttonGroup.getSelection().getActionCommand();
        System.out.println(searchBy);
        if (searchBy == "1"){
            try{
                searchBooks = DatabaseConnector.browseBookByTitle(searchTextField.getText());
                DefaultTableModel model = (DefaultTableModel) searchTable.getModel();
                model.setRowCount(0);
                for(Book book : searchBooks){
                    if (book.isAvailability()){
                        Object[] row = new Object[4];
                        row[0] = book.getBookId();
                        row[1] = book.getTitle();
                        row[2] = book.getAuthor();
                        row[3] = book.isAvailability();
                        model.addRow(row);
                    }
                }
            } catch( Exception e){
                JOptionPane.showMessageDialog(this,e.getMessage());            
            }
        }
        else if(searchBy == "2"){
            try{
                searchBooks = DatabaseConnector.browseBookByAuthor(searchTextField.getText());
                DefaultTableModel model = (DefaultTableModel) searchTable.getModel();
                model.setRowCount(0);
                for(Book book : searchBooks){
                    Object[] row = new Object[4];
                    row[0] = book.getBookId();
                    row[1] = book.getTitle();
                    row[2] = book.getAuthor();
                    row[3] = book.isAvailability();
                    model.addRow(row);
                }
            } catch( Exception e){
                JOptionPane.showMessageDialog(this,e.getMessage());            
            }
        }
        else{
            JOptionPane.showMessageDialog(null, "Chose one field Title or Author", "Search", HEIGHT);
        }
    }//GEN-LAST:event_searchButtonActionPerformed

